import pathlib
import re
from typing import Tuple, Union

import matplotlib.pyplot as plt
import numpy as np
import pandas as pd


class QuestionnaireAnalysis:
    """
    Reads and analyzes data generated by the questionnaire experiment.
    Should be able to accept strings and pathlib.Path objects.
    """

    def __init__(self, data_fname: Union[pathlib.Path, str]):
        if isinstance(data_fname, str):
            data_fname = pathlib.Path(data_fname)

        if not isinstance(data_fname, pathlib.Path):
            raise TypeError("data_fname must be a string or pathlib.Path object.")

        if not data_fname.is_file():
            raise ValueError(f"Data file does not exist: {data_fname}")

        self.data_fname = data_fname

    def read_data(self):
        """Reads the json data located in self.data_fname into memory, to
        the attribute self.data.
        """
        self.data = pd.read_json(self.data_fname)
        return self.data

    def show_age_distrib(self) -> Tuple[np.ndarray, np.ndarray]:
        """Calculates and plots the age distribution of the participants.

    Returns
    -------
    hist : np.ndarray
      Number of people in a given bin
    bins : np.ndarray
      Bin edges
        """
        data = self._get_data()
        ages = data["age"].dropna()
        bins = np.arange(0, 101, 10)

        hist, bins = np.histogram(ages, bins=bins)

        plt.figure()
        plt.hist(ages, bins=bins)
        plt.xlabel("Age")
        plt.ylabel("Number of participants")
        plt.title("Age distribution")

        return hist, bins

    def remove_rows_without_mail(self) -> pd.DataFrame:
        """Checks self.data for rows with invalid emails, and removes them.

    Returns
    -------
    df : pd.DataFrame
      A corrected DataFrame, i.e. the same table but with the erroneous rows removed and
      the (ordinal) index after a reset.
        """
        data = self._get_data()
        valid_email_mask = data["email"].apply(self._is_valid_email)
        return data.loc[valid_email_mask].reset_index(drop=True)

    def fill_na_with_mean(self) -> Tuple[pd.DataFrame, np.ndarray]:
        """Finds, in the original DataFrame, the subjects that didn't answer
        all questions, and replaces that missing value with the mean of the
        other grades for that student.

    Returns
    -------
    df : pd.DataFrame
        The corrected DataFrame after insertion of the mean grade
    arr : np.ndarray
        Row indices of the students that their new grades were generated
        """
        data = self._get_data()
        df = data.copy()

        question_cols = self._question_columns(df)
        questions = df.loc[:, question_cols].apply(pd.to_numeric, errors="coerce")

        rows_with_missing = questions.isna().any(axis=1)
        corrected_rows = questions.index[rows_with_missing].to_numpy()

        row_means = questions.mean(axis=1, skipna=True)
        filled_questions = questions.copy()

        for col in question_cols:
            missing_in_col = filled_questions[col].isna()
            filled_questions.loc[missing_in_col, col] = row_means.loc[missing_in_col]

        df.loc[:, question_cols] = filled_questions
        return df, corrected_rows

    def score_subjects(self, maximal_nans_per_sub: int = 1) -> pd.DataFrame:
        """Calculates the average score of a subject and adds a new "score" column
        with it.

        If the subject has more than "maximal_nans_per_sub" NaN in his grades, the
        score should be NA. Otherwise, the score is simply the mean of the other grades.
        The datatype of score is UInt8, and the floating point raw numbers should be
        rounded down.

        Parameters
        ----------
        maximal_nans_per_sub : int, optional
            Number of allowed NaNs per subject before giving a NA score.

        Returns
        -------
        pd.DataFrame
            A new DF with a new column - "score".
        """
        if not isinstance(maximal_nans_per_sub, int):
            raise TypeError("maximal_nans_per_sub must be an integer.")

        if maximal_nans_per_sub < 0:
            raise ValueError("maximal_nans_per_sub must be non-negative.")

        data = self._get_data()
        df = data.copy()

        question_cols = self._question_columns(df)
        questions = df.loc[:, question_cols].apply(pd.to_numeric, errors="coerce")

        missing_counts = questions.isna().sum(axis=1)
        raw_scores = np.floor(questions.mean(axis=1, skipna=True))
        raw_scores = raw_scores.mask(missing_counts > maximal_nans_per_sub)

        df["score"] = raw_scores.astype("UInt8")
        return df

    def correlate_gender_age(self) -> pd.DataFrame:
        """Looks for a correlation between the gender of the subject, their age
        and the score for all five questions.

    Returns
    -------
    pd.DataFrame
        A DataFrame with a MultiIndex containing the gender and whether the subject is above
        40 years of age, and the average score in each of the five questions.
    """
        data = self._get_data()
        question_cols = self._question_columns(data)

        df = data.copy()
        indexed_df = df.set_index([df.index, "gender", "age"])
        indexed_df.index.names = ["ordinal_index", "gender", "age"]

        valid_age_mask = indexed_df.index.get_level_values("age").notna()
        indexed_df = indexed_df.loc[valid_age_mask]

        gender_index = indexed_df.index.get_level_values("gender")
        above_40_index = indexed_df.index.get_level_values("age") > 40

        result = indexed_df.groupby([gender_index, above_40_index])[question_cols].mean()
        result.index.names = ["gender", "age"]

        return result

    def _get_data(self) -> pd.DataFrame:
        if not hasattr(self, "data"):
            raise AttributeError("Data was not loaded. Call read_data() first.")
        return self.data

    @staticmethod
    def _question_columns(df: pd.DataFrame) -> list[str]:
        question_cols = [
            col for col in df.columns
            if isinstance(col, str) and re.fullmatch(r"q\d+", col)
        ]

        return sorted(question_cols, key=lambda col: int(col[1:]))

    @staticmethod
    def _is_valid_email(email: object) -> bool:
        if not isinstance(email, str):
            return False

        if email.count("@") != 1:
            return False

        if email.startswith("@") or email.endswith("@"):
            return False

        if "." not in email:
            return False

        if email.startswith(".") or email.endswith("."):
            return False

        at_index = email.index("@")
        if email[at_index + 1] == ".":
            return False

        return True